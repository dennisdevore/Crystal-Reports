CREATE OR REPLACE VIEW ALPS.SHIPPINGPLATEVIEW
(
    LPID,
    ITEM,
    CUSTID,
    FACILITY,
    LOCATION,
    STATUS,
    HOLDREASON,
    UNITOFMEASURE,
    QUANTITY,
    TYPE,
    FROMLPID,
    SERIALNUMBER,
    LOTNUMBER,
    LOTNUMBERNOTNULL,
    PARENTLPID,
    USERITEM1,
    USERITEM2,
    USERITEM3,
    LASTUSER,
    LASTUPDATE,
    INVSTATUS,
    QTYENTERED,
    ORDERITEM,
    UOMENTERED,
    INVENTORYCLASS,
    LOADNO,
    STOPNO,
    SHIPNO,
    ORDERID,
    SHIPID,
    WEIGHT,
    UCC128,
    LABELFORMAT,
    TASKID,
    DROPSEQ,
    STATUSABBREV,
    INVSTATUSABBREV,
    INVENTORYCLASSABBREV,
    UOMABBREV,
    TYPEABBREV,
    ORDERLOT,
    ORDERLOTNOTNULL,
    PICKUOM,
    PICKUOMABBREV,
    PICKQTY,
    SHIPPINGPLATEROWID,
    TRACKINGNO,
    ITEMDESCR,
    OPENFACILITY,
    ACTUALCARRIER,
    CARRIERCODEUSED,
    SHIPPINGCOST,
    TOTELPID,
    weight_kgs,
    manufacturedate,
    expirationdate,
    QUANTITY_CASE,
    QUANTITY_ROLL,
    HEIGHT,
    LENGTH,
    WIDTH,
    PALLET_WEIGHT
)
AS
select
shippingplate.LPID,
shippingplate.ITEM,
shippingplate.CUSTID,
shippingplate.FACILITY,
shippingplate.LOCATION,
shippingplate.STATUS,
shippingplate.HOLDREASON,
shippingplate.UNITOFMEASURE,
shippingplate.QUANTITY,
shippingplate.TYPE,
shippingplate.FROMLPID,
shippingplate.SERIALNUMBER,
shippingplate.LOTNUMBER,
nvl(shippingplate.LOTNUMBER,'(none)'),
shippingplate.PARENTLPID,
shippingplate.USERITEM1,
shippingplate.USERITEM2,
shippingplate.USERITEM3,
shippingplate.LASTUSER,
shippingplate.LASTUPDATE,
shippingplate.INVSTATUS,
shippingplate.QTYENTERED,
shippingplate.ORDERITEM,
shippingplate.UOMENTERED,
shippingplate.INVENTORYCLASS,
shippingplate.LOADNO,
shippingplate.STOPNO,
shippingplate.SHIPNO,
shippingplate.ORDERID,
shippingplate.SHIPID,
shippingplate.WEIGHT,
shippingplate.UCC128,
shippingplate.LABELFORMAT,
shippingplate.TASKID,
shippingplate.DROPSEQ,
shippingplatestatus.abbrev,
inventorystatus.abbrev,
inventoryclass.abbrev,
unitsofmeasure.abbrev,
shippingplatetypes.abbrev,
shippingplate.orderlot,
nvl(shippingplate.orderlot,'(none)'),
shippingplate.pickuom,
substr(zit.uom_abbrev(pickuom),1,12),
shippingplate.pickqty,
shippingplate.rowid,
shippingplate.trackingno,
substr(zit.item_descr(shippingplate.custid,shippingplate.item),1,255),
shippingplate.openfacility,
actualcarrier,
carriercodeused,
shippingcost,
shippingplate.totelpid,
zwt.from_lbs_to_kgs(shippingplate.custid,shippingplate.weight),
shippingplate.manufacturedate,
shippingplate.expirationdate,
nvl(zbut.translate_uom_function(shippingplate.custid,shippingplate.item,shippingplate.quantity,shippingplate.unitofmeasure,
    nvl((select min(code) from unitsofmeasure where abbrev='Case'),'CS')),0),
nvl(zbut.translate_uom_function(shippingplate.custid,shippingplate.item,shippingplate.quantity,shippingplate.unitofmeasure,
    nvl((select min(code) from unitsofmeasure where abbrev='Roll'),'ROLL')),0),
shippingplate.HEIGHT,
shippingplate.LENGTH,
shippingplate.WIDTH,
shippingplate.PALLET_WEIGHT
from
shippingplate, inventoryclass, inventorystatus,
unitsofmeasure, shippingplatestatus, shippingplatetypes
where shippingplate.inventoryclass = inventoryclass.code(+)
  and shippingplate.invstatus = inventorystatus.code(+)
  and shippingplate.unitofmeasure = unitsofmeasure.code(+)
  and shippingplate.status = shippingplatestatus.code(+)
  and shippingplate.type = shippingplatetypes.code(+);

comment on table SHIPPINGPLATEVIEW  is '$Id$';

exit;
